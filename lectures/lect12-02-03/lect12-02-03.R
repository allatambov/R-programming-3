# напоминание: как выглядит цикл for

for (i in 1:10){
  print(i)
}

# как выйти из цикла for
# например, выводим на экран числа от 1 до 10,
# но прекращаем, если "натыкаемся" на число, равное 5

for (i in 1:10){
  print(i)
  if (i == 5){
    break
  }
}

