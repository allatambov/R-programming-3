# существует другой цикл - цикл while
# в отличие от цикла for, ему нужно задавать начало - значение, с которого мы стартуем
# цикл while выполняется до тех пор, пока верно некоторое условие (в круглых скобках)

i <- 1 # начинаем с 1

while (i < 5){
	print(i)
	i <- i + 1
}

# мы стартуем со значения 1, выводим число на экран, затем прибавляем 1 - переходим к следующему
# если убрать строчку i <- i + 1, все зациклится
# если зациклились - нажмите красный значок Stop в консоли (в правом верхнем углу)

# так зациклмся!

 while (i < 5){
	print(i)
}

